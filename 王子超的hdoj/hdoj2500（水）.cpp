#include<stdio.h>
int main()
{
    int N,M,i,j;
    while(scanf("%d",&N)!=EOF)
    {
        while(N--)
        {
            scanf("%d",&M);
            for(i=1;i<=3*M;i++)
            {
                for(j=1;j<=M;j++)
                    printf("HDU");
                printf("\n");
            }
        }
    }
}
